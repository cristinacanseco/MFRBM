package com.example.llorar.Muestreo;

public class Dimension {

    public int ancho;
    public int alto;

    public Dimension(int ancho, int alto) {
        this.ancho = ancho;
        this.alto = alto;
    }

    public int getAncho() {
        return ancho;
    }

    public int getAlto() {
        return alto;
    }
}
