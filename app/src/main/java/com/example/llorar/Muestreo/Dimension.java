package com.example.llorar.Muestreo;

public class Dimension {

    public double ancho;
    public double alto;

    public Dimension(double  ancho, double  alto) {
        this.ancho = ancho;
        this.alto = alto;
    }

    public double getAncho() {
        return ancho;
    }

    public double getAlto() {
        return alto;
    }
}
